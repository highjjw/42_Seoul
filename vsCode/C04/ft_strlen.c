#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != 0)
	{
		i++;
	}
	return (i);
}

int	main(void)
{
	char	test[] = "123456789";
	int i = ft_strlen(test);

	printf("%i\n", i);
		
}