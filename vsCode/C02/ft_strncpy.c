#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int main(){

    char dest[100];
    char src[100] = "Hello";
    
    
    printf("dest 배열값은: %s\n",ft_strncpy(dest,src,7));
    printf("%c",dest[4]);
}
